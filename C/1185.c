#include <stdio.h>

// 全局变量（简化递归函数参数传递，初学者更易理解）
int nums[4];    // 存储输入的4个数字
int used[4] = {0};  // 标记数字是否被使用，初始化为0（未使用）
int path[3];    // 存储当前构建的排列（长度为3）

/**
 * 递归回溯函数
 * @param depth 当前排列已构建的长度（已选数字个数）
 */
void backtrack(int depth) {
    // 1. 递归终止条件：当前排列长度达到3，输出该排列
    if (depth == 3) {
        printf("%d %d %d\n", path[0],path[1],path[2]);
        return; // 回溯到上一层，继续寻找其他排列
    }

    // 2. 遍历所有可选数字（4个输入数字）
    for (int i = 0; i < 4; i++) {
        // 3. 剪枝：跳过已被使用的数字（避免排列重复）
        if (used[i] == 1) 
        {
            continue;
        }  

        // 4. 试探：选择当前数字，加入路径，并标记为已使用
        path[depth] = nums[i];  // 把当前数字存入路径的对应位置（depth对应当前位置索引）
        used[i] = 1;            // 标记该数字已被使用

        // 5. 递归深入：构建下一层排列（已选数字个数+1）
        backtrack(depth + 1);

        // 6. 回溯：撤销上一步的选择，恢复状态（关键步骤）
        used[i] = 0;            // 取消数字的使用标记，供下一轮循环使用
        // path无需手动清空，下一轮循环会直接覆盖对应位置的值
    }
}

int main() {
    // 1. 输入4个互不相同的整数
    scanf("%d %d %d %d", &nums[0], &nums[1], &nums[2], &nums[3]);

    // 2. 调用递归回溯函数，初始深度为0（尚未选择任何数字）
    backtrack(0);

    return 0;
}